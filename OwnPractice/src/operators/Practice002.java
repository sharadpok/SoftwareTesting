package operators;

import com.sun.security.jgss.GSSUtil;

public class Practice002 {
    public static void main(String[] args) {

        // Unary Operators

        int a=10;
        a+=10; // a=a+10;
        System.out.println("Updated value: "+a);  // 20

        int b=20;
        b-=10; // b=b-10;
        System.out.println("Updated Value: "+b); // 10

        int c=30;
        c*=10; // c=c*10;
        System.out.println("Updated Value: "+c); // 300

        int d=12;
        d/=4;// d=d/4;
        System.out.println("Updated Value: "+d); // 3


    }
}
