package stringClass;

public class Practice003 {

    public static void main(String[] args) {
        //  Ternary operator.
        //  ? if true do this : ele do that

        int a1 = (30>40) ? 10: 20;
        System.out.println(a1);  // 20

        String str= 10 > 20 ? "10" : "TWENTY";
        System.out.println(str);  // TWENTY

    }
}
