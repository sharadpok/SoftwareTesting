package dataTypes;

public class Practice007 {
    public static void main(String[] args) {
        // formatting, %d - For integer part
        int n = 9;
        System.out.printf("Your number is ->%d\n",n);

        // %s -For String
        String name = "Pramod";
        System.out.printf("Your name is -> %s\n",name);

        // %f -floating points
        float pi = 3.14f;
        System.out.printf("pi value is -> %f\n",pi);
    }
}
