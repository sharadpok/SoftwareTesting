package dataTypes;

public class Practice001 {
    public static void main(String[] args) {
         // Premitive Data types:
        // boolean = 1 bit
        boolean b=true;
        System.out.println("I am boolean type: "+b);

        // Integral type data types
        // byte = 1 byte
        byte b1 = 10;
        System.out.println("I am byte type: "+b1);

        // short = 2 bytes
        short s=8000;
        System.out.println("I am short type: "+s);

        // int = 4 bytes
        int i= 456566;
        System.out.println("I am int type: "+i);

        // long =8 bytes
        long l= 5667845678656L;
        System.out.println("I am long type: "+l);

        // char = 1 byte
        char c='A';
        System.out.println("I am char type: "+c);

        // Floating type data types:

        // Float = 4 bytes
        float f=45.45f;
        System.out.println("I am float type: "+f);

        // double = 8 bytes
        double d= 45.564496867593;
        System.out.println("I am double type: "+d);



    }
}
