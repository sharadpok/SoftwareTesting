package dataTypes;

public class Practice003 {
    public static void main(String[] args) {
        // Concatenation

        String s1= "Sharad";
        String s2 = "Pokale";

        byte n1= 12;
        byte n2= 6;

        System.out.println(s1+s2+n1+n2);   // SharadPokale126
        System.out.println(n1+s1+s2);      // 12SharadPokale
        System.out.println(s1+n2+s1);      // Sharad6Sharad
        System.out.println(n1+n2+s1+n1+n2); //18Sharad126





    }
}
